# Addition
a = 10
b = 5
print(a + b)  # Output: 15


# Subtraction (-)
a = 10
b = 5
print(a - b)  # Output: 5


# Multiplication (*)
a = 10
b = 5
print(a * b)  # Output: 50

# Division (/)
a = 10
b = 4
print(a / b)  # Output: 2.5


# Modulus (%)
a = 10
b = 4
print(a % b)  # Output: 2

# Exponentiation (**)
a = 2
b = 5
print(a ** b)  # Output: 32
# 2*2*2*2*2


# Floor Division (//)
a = 10
b = 4
# result = 2.9
print(a // b)  # Output: 2

a = -10
b = 4
# result = -2.5
print(a // b)  # Output: -3

# comparison Operators

x = 10
y = 20

print("Equal to:", x == y)        # False
print("Not equal to:", x != y)   # True
print("Greater than:", x > y)    # False
print("Less than:", x < y)       # True
print("Greater or equal:", x >= y)  # False
print("Less or equal:", x <= y)  # True
