# Basic Assignment Operator (=)

x = 5  # Assigns 5 to x

# Compound Assignment Operators
# Add and Assign (+=)
a = 10

a = a+20

a += 30


print(a)


# Subtract and Assign (-=)
sub = 10
sub -= 3  # Equivalent to sub = sub - 3
print(sub)  # Output: 7

# Multiply and Assign (*=)
mul = 4
mul *= 2  # Equivalent to mul = mul * 2
print(mul)  # Output: 8

# Divide and Assign (/=)
div = 8
div /= 2  # Equivalent to div = div / 2
print(div)  # Output: 4.0
